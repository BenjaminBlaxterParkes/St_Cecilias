        <h2>About the Exhibition</h2>
        <h3>Opening: 27 July 2018 | Where: Main Library Exhibition Gallery – Ground Floor | Closing: 27 October 2018 </h3>
        <p>
            Featuring Copernicus’ 1543 ground-breaking book On the Revolutions of Heavenly Spheres, meteorites, star charts,
            scientific instruments and Katie Paterson’s contemporary artwork Timepieces, this exhibition charts our fascination with,
            exploration of and responses to outer space.
        </p>
        <p>
            This exhibition will run in partnership with Ingleby Gallery’s
            Edinburgh Arts Festival exhibition <a href="http://www.inglebygallery.com/exhibitions/jacobs-ladder/" target="_blank">Jacob&#39;s Ladder</a>.
            A display celebrating mankind’s relationship with space and our enduring attempts to fathom the unfathomable.
        </p>
