    <p>
     Art and Anatomy are often treated as distinct subjects, yet there is a long history of collaboration between the
        disciplines. This exhibition gives visitors the opportunity to explore examples of this rich exchange of expertise.
        Through illustrations, models and virtual technologies, the close relationship between visual art and the science
        of anatomy will be revealed.
    </p>
    <p>
    From the sixteenth century, artists have been employed to work alongside anatomists to create visual representations
        of their research. The development of anatomical models required specific technical knowledge. Whilst some models
        were built by specialised craftspeople working with traditional techniques, others were mass produced and readily
        available to buy through trade catalogues. Despite the arrival of new materials like plastic in the twentieth
        century, older materials like wax were ideally suited for model making and continued to be used.
    </p>
    <p>
    Today, technology continues to advance and new forms of anatomical representation are being created. Three-dimensional
        scanning and printing have enabled anatomists to produce bespoke models both quickly and cheaply, and animated
        holograms encourage new ways to visualise anatomy.
    </p>
    <p>
    Many of the objects in this exhibition are from the collections of the University of Edinburgh Anatomical Museum
        and have never been on public display before.
    </p>
