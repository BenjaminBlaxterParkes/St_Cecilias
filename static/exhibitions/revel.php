        <div class="content">
            <p>Edinburgh College of Art’s yearly Revel, which dates back to 1909, brings together staff and students for
                a themed party with costumes, scenery, performances and dancing.</p>

            <p>This exhibition in the University of Edinburgh’s Main Library, includes a number of unique photographs of
                students at the Revel from the 1910s to the 1970s, a range of colourful ticket and programme designs,
                and a costume thought to have been part of the 1951 Revel - which had a Knights of the Round Table theme.</p>

            <p>Former student and celebrated artist John Bellany is known to have designed the set for the 1963 Revel.
                Former College Principal and painter William Gillies also features in a silent film showing students
                creating props and enjoying the event.</p>
