        <h1>IIIF</h1>

           <h3> <img src="https://upload.wikimedia.org/wikipedia/commons/e/e8/International_Image_Interoperability_Framework_logo.png" class ="iiifstatic"></h3>
        <p>
            The University of Edinburgh has adopted IIIF as our standard for image interoperability and workflow. This has a number of advantages for us, including:
            <ul>
                <li>High resolution image zooming in our collections item pages.</li>
                <li>The ability to share out our content using URLs (manifests).</li>
                <li>The ability to pull in other IIIF-compliant insititutions' content using URLs (manifests).</li>
                <li>On-the-fly tweaking of images without the need to physically access the original.</li>
                <li>A culture of access to images through URLs, precluding the need to store images, thus saving server space.</li>
            </ul>

            <h3><img src="http://images.is.ed.ac.uk/luna/images/LUNAIIIF80.png" class=""></h3>
<p>
            This collection makes use of IIIF. On the record page, you will notice we have embedded high resolution zooming, using the OpenSeadragon image viewer.
            This has been made possible by our high-resolution image platform, LUNA (<a href="http://images.is.ed.ac.uk">http://images.is.ed.ac.uk</a>), becoming IIIF-compliant.

            If you are familiar with the IIIF viewers Mirador and Universal Viewer, then you can add these images into these items by right-clicking on either of the logos to pick up the manifests.

            Further information is available on the site, at <a href="http://iiif.io" title="IIIF site" target ="_blank">iiif.io</a>.
        </p>
