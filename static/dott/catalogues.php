        <h1 class="itemtitle">How to use the catalogues</h1>
            <p>LHSA’s anonymised case note catalogues are accessed from a section of the University of Edinburgh’s online
                    archive catalogue, ArchivesSpace:</p>
            <p>Neurosurgery case notes (Norman Dott)
            There are four main groups (or series) of case notes in the Dott collections:</p>
        <p>
            <ul>
                <li>Private practice case notes (LHB1 CC/20/PR1)</li>
                <li>Royal Infirmary of Edinburgh, Ward 20 case notes (LHB1 CC/24/PR2)</li>
                <li>Second World War case notes from Bangour General (Emergency Medical Service) Hospital (LHB40 CC/2/PR3)</li>
                <li>Filed by Disease case notes (LHB1 CC/22/PR4)</li>
            </ul>
        </p>
            <p>The catalogue description for each series of case notes describes the historical context behind the files
                    and the types of documents represented in them.</p>

            <p>We have tried to make the item-level catalogue descriptions of the case notes accessible to everyone by using
                    different language in different parts of them. For example, we describe medical conditions in modern
                    clinical terms taken from the <a href="https://www.nlm.nih.gov/mesh/MBrowser.html" target="_blank">
                    Medical Subject Headings thesaurus (MeSH)</a> as ‘subjects’, whereas medical conditions are expressed
                    in terms from the case notes themselves in the ‘Scope and Contents’ part of the description. For
                    example, a male patient might be described as suffering from ‘fainting’ in the text of the case
                    notes (which we record in the ‘Scope and Contents’ part of the catalogue entry), but in the ‘Subject’
                    field, we would describe his condition as ‘Syncope’ (the modern medical term used for fainting).
                    </p>

            <p>Catalogues can be searched from this page by medical condition, visible in menus at the side of the screen.
                    Clicking on a condition will take you to a list of case notes which mention this condition. You can
                    also search by some of the key characteristics of the case notes that we have highlighted in each
                    catalogue entry:</p>
        <p>
            <ul>
                    <li>Patient age: entering an age in the search box will bring up all the patients of this age.</li>
                    <li>Patient sex: entering ‘female’ or ‘male’ in the search box will bring up cases by sex.</li>
                    <li>Treatment: patients have been recorded as having ‘surgical’, ‘nonsurgical’ or ‘no treatment’.
                            Entering either one of these terms will bring up all patients recorded under these categories.</li>
                    <li>Medical condition: medical condition can either be searched through entering a condition name in
                            the search box, or by clicking in the Subject menu bar of the catalogue page and entering a
                            condition (expressed as an authority term from <a href=https://www.nlm.nih.gov/mesh/MBrowser.html" target="_blank">MeSH</a>) in the ‘filter by text’ box.</li>
                    <li>Geographical origin of patient (Dott catalogues): in order to protect the anonymity of patients,
                            we have expressed where patients come from in four main ways: Edinburgh and the Lothians,
                            Scotland (outside Edinburgh and the Lothians), Great Britain (excluding Scotland) and outside
                            Great Britain. You can bring up all cases from one of these geographical areas by clicking in
                            the Subject menu bar of the catalogue page and entering one of the categories in the
                            ‘filter by text’ box.</li>
            </ul>
        </p>
            <p>There are certain aspects of the descriptions that we have removed from the cases to protect the anonymity
                    of patients in the public catalogues – these include the name of each patient and (in some cases)
                    their occupation. We can reveal other details about each case, since it would be hard to identify
                    the patient from those details – such as their age in years (rather than their birthdate) or whether
                    they are male or female. Sometimes, a form of information – such as medical condition - can be
                    revealed in certain circumstances (the occurrence of a common type of brain tumour would not in
                    itself identify someone), but not others (a rare, genetic condition might easily be linked to a
                    particular individual).</p>

            <p>We have also produced confidential, unredacted catalogues, which can be viewed by legitimate researchers who
            meet strict conditions on the use of patient-identifiable data. Access is by means of an application to NHS
            Lothian through the LHSA Archivist. These highly confidential resources can only be consulted in person in
            our reading room in the Centre for Research Collections, University of Edinburgh. Please contact us
            <a href="mailto:lhsa@ed.ac.uk">lhsa@ed.ac.uk</a> for further details.</p>
