    <h1>Towards Dolly: Edinburgh, Roslin and the Birth of Modern Genetics</h1>

        <p>
            Towards Dolly is a Wellcome Trust funded project based at the Centre for Research
            Collections (part of Library and University Collections), University of Edinburgh. We are making accessible
            to the public collections which document Edinburgh's key position in the development of genetics.</p>
        <div class="outer-image">
            <a href="./people" title="University of Edinburgh Institute of Animal Genetics staff"><div id="index-image" class="people-tile"></div></a>
        </div>
        <div class="clearfix"></div>
        <p>
            This project has catalogued, preserved and conserved internationally-important
            <a href="./catalogues" title="catalogue list">collections</a> which tell the
            story of how Edinburgh became one of the world's centres of genetics research.
            </p>
        <p class="quote"><q >Spanning as they do from before the rediscovery of Mendel's work to the birth of Dolly and stem cell
            research, these papers form a collection unrivalled in the history and development of Genetics.</q>
        </p>
